public class OperadoresAtribuição2 {
    public static void main(String[] args) {
        // Operador de Atribuição com Adição '+='
        // Este operador adiciona o valor da direita á variável da esquerda e atribui o resultado á variável da esquerda.
        int x = 10;
        x += 5; // Equivalente a x = x + 5
        System.out.println("O valor de x após x += 5 é: " + x);
    }
}
