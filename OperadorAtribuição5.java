public class OperadorAtribuição5 {
    public static void main(String[] args) {
        // Operador de Atribuição com Dvisão '/='
        // Este operador divide a variável da esquerda pelo valor da direita e atribui o resultado á variável da esquerda.
        int x = 20;
        x /= 4; // Equivalente a x = x / 4
        System.out.println("O valor da x após x /= 4 é:" + x);
    }
}