public class OperadorAtribuição3 {
    public static void main(String[] args) {
        // Operador de atribuição com Subtração '-='
        // Este operador subtrai o valor da direita da variável da esquerda e atribui o resultado á variável da esquerda.
        int x = 10;
        x -= 3; // Equivalente a x = x - 3
        System.out.println("O valor de x após x -= 3 é: " + x);
    }
}
