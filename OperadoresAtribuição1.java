public class OperadoresAtribuição1 {
    public static void main(String[] args) {
        // Operador de Atribuição Simples '='
        // Este operador é usado para atribuir um valor a uma variável.
        int x = 10; // Atribui 10 á variável x
        System.out.println("O valor de x é: " + x);
    }
}
